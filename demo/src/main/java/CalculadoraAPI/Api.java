package CalculadoraAPI;

public class Api {

    public static int suma(int num, int num2){
        return num + num2;
    }



    public static int resta(int num, int num2){
        return num - num2;
    }



    public static int multi(int num, int num2){
        return  num * num2;
    }



    public static int div(int num, int num2){
        return num/num2;
    }




}
